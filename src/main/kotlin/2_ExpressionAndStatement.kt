fun main() {
    // Expression adalah sintaks yang menghasilkan nilai
    Integer.sum(20, 103)
    2 * 5 + 10 - 9 / 3

    // Statement adalah sintaks yang melakukan sesuatu (aksi)
    val x = 20 % 4 // mendeklarasi dan menginisialisasi variabel x
    println(x) // menampilkan nilai variabel x ke layar
}